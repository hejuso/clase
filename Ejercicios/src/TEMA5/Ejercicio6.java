package TEMA5;

public class Ejercicio6 {

	// Atributos

	public int x;
	public int y;

	// Constructor

	public Ejercicio6(int a, int b) {

		x = a;
		y = b;

	}

	// Métodos

	public void suma() {

		System.out.println("Suma de A + B = " + (x + y));

	}

	public void resta() {

		System.out.println("Resta de A - B = " + (x - y));

	}

	public void multiplicacion() {

		System.out.println("Multiplicación de A * B = " + (x * y));

	}

	public void division() {

		System.out.println("División de A / B = " + (x / y));

	}

}
